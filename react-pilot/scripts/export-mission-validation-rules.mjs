#!/usr/bin/env node
/**
 * Emit markdown from `missionValidationRuleSets` (src/profiles/mission/missionValidationRules.js):
 * 1. Flat rule table → docs/generated/mission-validation-rules.generated.md
 * 2. Rules grouped by `pilotState` field → docs/generated/mission-validation-by-field.generated.md
 *
 * Usage (from react-pilot/):
 *   node scripts/export-mission-validation-rules.mjs
 *   node scripts/export-mission-validation-rules.mjs --stdout   (flat table only, to stdout)
 *
 * @module scripts/export-mission-validation-rules
 */

import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import { missionValidationRuleSets } from '../src/profiles/mission/missionValidationRules.js'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const OUT_FLAT = path.join(__dirname, '../docs/generated/mission-validation-rules.generated.md')
const OUT_BY_FIELD = path.join(__dirname, '../docs/generated/mission-validation-by-field.generated.md')

/** @type {readonly string[]} */
const PREFIX_ORDER = ['mission', 'platform', 'sensors', 'keywords', 'spatial', 'distribution']

function escCell(s) {
  return String(s ?? '')
    .replace(/\|/g, '\\|')
    .replace(/\r?\n/g, ' ')
    .trim()
}

function ruleNote(rule) {
  const src = rule.check?.toString() ?? ''
  if (src.includes('abstractQualityIssues')) {
    return 'Expands to 0–3 abstract quality warnings (length, platform/sensor mention, acronyms).'
  }
  if (src.includes('sensorRowIsInactive') && rule.field === 'sensors') {
    return 'Per active sensor row: type, modelId/sensorId, variable; optional GCMD alignment warning.'
  }
  if (rule.field === 'keywords' && String(rule.message || '').includes('facets')) {
    return 'One error row per empty GCMD facet (sciencekeywords, datacenters, …).'
  }
  if (rule.field === 'keywords' && String(rule.message || '').includes('UUID')) {
    return 'One warning per keyword chip with missing/invalid GCMD concept UUID.'
  }
  return ''
}

/**
 * @returns {Array<{ setId: string, modes: string, field: string, severity: string, message: string, xpath: string, note: string }>}
 */
function collectRows() {
  const rows = []
  for (const set of missionValidationRuleSets) {
    const modes = (set.modes || []).join(', ')
    for (const rule of set.rules || []) {
      rows.push({
        setId: set.id,
        modes,
        field: rule.field,
        severity: rule.severity,
        message: rule.message,
        xpath: rule.xpath ?? '',
        note: ruleNote(rule),
      })
    }
  }
  return rows
}

function fieldPrefix(field) {
  const dot = field.indexOf('.')
  if (dot === -1) return field
  return field.slice(0, dot)
}

function compareFields(a, b) {
  const pa = fieldPrefix(a)
  const pb = fieldPrefix(b)
  const ia = PREFIX_ORDER.indexOf(pa)
  const ib = PREFIX_ORDER.indexOf(pb)
  const ra = ia === -1 ? 999 : ia
  const rb = ib === -1 ? 999 : ib
  if (ra !== rb) return ra - rb
  return a.localeCompare(b)
}

function buildMarkdownFlat(rows, iso) {
  let md = `<!-- AUTO-GENERATED by \`node scripts/export-mission-validation-rules.mjs\` — do not edit by hand. -->
<!-- Generated at: ${iso} -->

# Mission profile — validation rules (generated)

This table is produced from \`missionValidationRuleSets\` in [\`src/profiles/mission/missionValidationRules.js\`](../src/profiles/mission/missionValidationRules.js). It is the **authoritative list** of rule definitions the wizard / Validator / Lens use for the **mission** ISO 19115-2 pilot (together with \`validatePilotState\` parity tests in \`verify-pilot.mjs\`).

**Modes:** \`lenient\` | \`strict\` | \`catalog\` — a rule runs only when the active mode is listed in **Rule set modes** for that set.

**Severity:** \`e\` = error (blocks a clean score), \`w\` = warning.

| Rule set | Rule set modes | \`pilotState\` field (primary) | Sev | Message | XPath (preview) | Notes |
| ---------- | -------------- | ------------------------------ | --- | --------- | --------------- | ----- |
`
  for (const r of rows) {
    md += `| ${escCell(r.setId)} | ${escCell(r.modes)} | \`${escCell(r.field)}\` | ${escCell(r.severity)} | ${escCell(r.message)} | ${r.xpath ? `\`${escCell(r.xpath)}\`` : ''} | ${escCell(r.note)} |\n`
  }

  md += `
---

## Related docs

- [mission-validation-by-field.generated.md](./mission-validation-by-field.generated.md) — **same rules, grouped by \`pilotState\` field** (field × validation matrix).
- [mission-validation-by-area.md](../mission-validation-by-area.md) — tables by **GCMD facet**, sensors, wizard area, and mode extras.
- [uxs-ncei-template-mission-pilot-matrix.md](./uxs-ncei-template-mission-pilot-matrix.md) — NCEI template ↔ \`pilotState\` ↔ XML **import** matrix (not every row has a validation rule here).
- [\`pilotValidation.js\`](../src/lib/pilotValidation.js) — \`validatePilotState(mode, state)\` must stay aligned with these rules (parity test).
`
  return md
}

function buildMarkdownByField(rows, iso) {
  const byField = /** @type {Map<string, typeof rows>} */ (new Map())
  for (const r of rows) {
    const k = r.field
    if (!byField.has(k)) byField.set(k, [])
    byField.get(k).push(r)
  }
  const fields = [...byField.keys()].sort(compareFields)

  let md = `<!-- AUTO-GENERATED by \`node scripts/export-mission-validation-rules.mjs\` — do not edit by hand. -->
<!-- Generated at: ${iso} -->

# Mission validation — by \`pilotState\` field (generated matrix)

Each **\`pilotState\`** path below lists **every validation rule** that uses it as the primary \`field\` in [\`missionValidationRules.js\`](../src/profiles/mission/missionValidationRules.js). Multi-issue rules (\`keywords.*\`, \`sensors[]\`) still appear once per **rule definition**; runtime may emit **multiple** issue rows (see **Notes**).

**How to read:** **Rule set modes** = validation modes when this rule runs. **Sev** = \`e\` (error) or \`w\` (warning).

For **NCEI XML ↔ field** mapping (import), see [uxs-ncei-template-mission-pilot-matrix.md](../uxs-ncei-template-mission-pilot-matrix.md).

---

`
  for (const field of fields) {
    const list = byField.get(field) || []
    md += `## \`${escCell(field)}\`\n\n`
    md += '| Rule set | Rule set modes | Sev | Message | XPath (preview) | Notes |\n'
    md += '| -------- | -------------- | --- | ------- | --------------- | ----- |\n'
    for (const r of list) {
      md += `| ${escCell(r.setId)} | ${escCell(r.modes)} | ${escCell(r.severity)} | ${escCell(r.message)} | ${r.xpath ? `\`${escCell(r.xpath)}\`` : ''} | ${escCell(r.note)} |\n`
    }
    md += '\n'
  }

  md += `---

## Related docs

- [mission-validation-rules.generated.md](./mission-validation-rules.generated.md) — flat table (all rules in one list).
- [mission-validation-by-area.md](../mission-validation-by-area.md) — GCMD facets, sensors, mode summaries.
- [uxs-ncei-template-mission-pilot-matrix.md](../uxs-ncei-template-mission-pilot-matrix.md) — import / XML matrix.
`
  return md
}

const iso = new Date().toISOString()
const rows = collectRows()
const mdFlat = buildMarkdownFlat(rows, iso)
const mdByField = buildMarkdownByField(rows, iso)

const useStdout = process.argv.includes('--stdout')

if (useStdout) {
  process.stdout.write(mdFlat)
} else {
  const dir = path.dirname(OUT_FLAT)
  fs.mkdirSync(dir, { recursive: true })
  fs.writeFileSync(OUT_FLAT, mdFlat, 'utf8')
  fs.writeFileSync(OUT_BY_FIELD, mdByField, 'utf8')
  console.error(`Wrote ${path.relative(path.join(__dirname, '..'), OUT_FLAT)} (${mdFlat.split('\n').length} lines)`)
  console.error(`Wrote ${path.relative(path.join(__dirname, '..'), OUT_BY_FIELD)} (${mdByField.split('\n').length} lines)`)
}
