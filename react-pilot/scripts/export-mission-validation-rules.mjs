#!/usr/bin/env node
/**
 * Emit a markdown table of all mission profile validation rules (source of truth:
 * src/profiles/mission/missionValidationRules.js).
 *
 * Usage (from react-pilot/):
 *   node scripts/export-mission-validation-rules.mjs
 *   node scripts/export-mission-validation-rules.mjs --stdout
 *
 * Default: writes docs/generated/mission-validation-rules.generated.md
 *
 * @module scripts/export-mission-validation-rules
 */

import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import { missionValidationRuleSets } from '../src/profiles/mission/missionValidationRules.js'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const OUT = path.join(__dirname, '../docs/generated/mission-validation-rules.generated.md')

function escCell(s) {
  return String(s ?? '')
    .replace(/\|/g, '\\|')
    .replace(/\r?\n/g, ' ')
    .trim()
}

function ruleNote(rule) {
  const src = rule.check?.toString() ?? ''
  if (src.includes('abstractQualityIssues')) {
    return 'Expands to 0–3 abstract quality warnings (length, platform/sensor mention, acronyms).'
  }
  if (src.includes('sensorRowIsInactive') && rule.field === 'sensors') {
    return 'Per active sensor row: type, modelId/sensorId, variable; optional GCMD alignment warning.'
  }
  if (rule.field === 'keywords' && String(rule.message || '').includes('facets')) {
    return 'One error row per empty GCMD facet (sciencekeywords, datacenters, …).'
  }
  if (rule.field === 'keywords' && String(rule.message || '').includes('UUID')) {
    return 'One warning per keyword chip with missing/invalid GCMD concept UUID.'
  }
  return ''
}

function buildMarkdown() {
  const rows = []
  for (const set of missionValidationRuleSets) {
    const modes = (set.modes || []).join(', ')
    for (const rule of set.rules || []) {
      rows.push({
        setId: set.id,
        modes,
        field: rule.field,
        severity: rule.severity,
        message: rule.message,
        xpath: rule.xpath ?? '',
        note: ruleNote(rule),
      })
    }
  }

  const iso = new Date().toISOString()
  let md = `<!-- AUTO-GENERATED by \`node scripts/export-mission-validation-rules.mjs\` — do not edit by hand. -->
<!-- Generated at: ${iso} -->

# Mission profile — validation rules (generated)

This table is produced from \`missionValidationRuleSets\` in [\`src/profiles/mission/missionValidationRules.js\`](../src/profiles/mission/missionValidationRules.js). It is the **authoritative list** of rule definitions the wizard / Validator / Lens use for the **mission** ISO 19115-2 pilot (together with \`validatePilotState\` parity tests in \`verify-pilot.mjs\`).

**Modes:** \`lenient\` | \`strict\` | \`catalog\` — a rule runs only when the active mode is listed in **Rule set modes** for that set.

**Severity:** \`e\` = error (blocks a clean score), \`w\` = warning.

| Rule set | Rule set modes | \`pilotState\` field (primary) | Sev | Message | XPath (preview) | Notes |
| ---------- | -------------- | ------------------------------ | --- | --------- | --------------- | ----- |
`
  for (const r of rows) {
    md += `| ${escCell(r.setId)} | ${escCell(r.modes)} | \`${escCell(r.field)}\` | ${escCell(r.severity)} | ${escCell(r.message)} | ${r.xpath ? `\`${escCell(r.xpath)}\`` : ''} | ${escCell(r.note)} |\n`
  }

  md += `
---

## Related docs

- [mission-validation-by-area.md](../mission-validation-by-area.md) — tables by **GCMD facet**, sensors, wizard area, and mode extras.
- [uxs-ncei-template-mission-pilot-matrix.md](./uxs-ncei-template-mission-pilot-matrix.md) — NCEI template ↔ \`pilotState\` ↔ XML **import** matrix (not every row has a validation rule here).
- [\`pilotValidation.js\`](../src/lib/pilotValidation.js) — \`validatePilotState(mode, state)\` must stay aligned with these rules (parity test).
`
  return md
}

const md = buildMarkdown()
const useStdout = process.argv.includes('--stdout')

if (useStdout) {
  process.stdout.write(md)
} else {
  const dir = path.dirname(OUT)
  fs.mkdirSync(dir, { recursive: true })
  fs.writeFileSync(OUT, md, 'utf8')
  console.error(`Wrote ${path.relative(path.join(__dirname, '..'), OUT)} (${md.split('\n').length} lines)`)
}
