import fs from 'node:fs'
import path from 'node:path'

import { metadataListFromCometSearchJson } from '../../src/lib/cometSearchPayload.js'

function parseArgs(argv) {
  const args = {
    cometBase: process.env.COMET_BASE_URL || 'https://data.noaa.gov/cedit',
    sessionId: process.env.COMET_SESSION_ID || '',
    recordGroup: 'Manta',
    query: '',
    max: 200,
    top: 10,
    outJson: 'fixtures/mission/_comet-search-results.json',
    outUuidFile: 'fixtures/mission/comet-uuids.txt',
  }
  for (let i = 0; i < argv.length; i += 1) {
    const a = argv[i]
    if (a === '--comet-base') args.cometBase = argv[++i]
    else if (a === '--session-id') args.sessionId = argv[++i]
    else if (a === '--record-group') args.recordGroup = argv[++i]
    else if (a === '--query') args.query = argv[++i]
    else if (a === '--max') args.max = Number(argv[++i] || '200')
    else if (a === '--top') args.top = Number(argv[++i] || '10')
    else if (a === '--out-json') args.outJson = argv[++i]
    else if (a === '--out-uuid-file') args.outUuidFile = argv[++i]
  }
  return args
}

function similarityScore(query, value) {
  const q = String(query || '').trim().toLowerCase()
  const v = String(value || '').trim().toLowerCase()
  if (!q || !v) return 0
  if (v === q) return 1
  if (v.includes(q)) return 0.93
  const qParts = q.split(/[^a-z0-9]+/).filter(Boolean)
  if (!qParts.length) return 0
  const hits = qParts.filter((p) => v.includes(p)).length
  return Math.min(0.9, hits / qParts.length)
}

async function searchComet({ cometBase, sessionId, recordGroup, max }) {
  const base = cometBase.replace(/\/$/, '')
  const url = new URL(`${base}/metadata/search`)
  url.searchParams.set('recordGroup', recordGroup)
  url.searchParams.set('format', 'json')
  url.searchParams.set('max', String(max))
  const res = await fetch(url.toString(), {
    method: 'GET',
    headers: {
      Accept: 'application/json',
      Cookie: `JSESSIONID=${sessionId}`,
    },
    redirect: 'manual',
  })
  const body = await res.text()
  if (res.status >= 300 && res.status < 400) {
    throw new Error(
      `CoMET search redirected (${res.status}) — session likely expired or not logged in. Refresh JSESSIONID.`,
    )
  }
  if (!res.ok) {
    throw new Error(`CoMET search failed (${res.status}): ${body.slice(0, 300)}`)
  }
  let parsed = null
  try {
    parsed = JSON.parse(body)
  } catch {
    throw new Error(`CoMET search returned non-JSON response: ${body.slice(0, 200)}`)
  }
  const rows = metadataListFromCometSearchJson(parsed)
  return rows.map((r) => ({
    uuid: String(r.uuid || '').trim(),
    fileIdentifier: String(r.fileIdentifier || '').trim(),
    name: String(r.name || '').trim(),
    editState: String(r.editState || '').trim(),
  })).filter((r) => r.uuid)
}

function selectTop(rows, query, top) {
  const ranked = rows
    .map((r) => {
      const score = Math.max(
        similarityScore(query, r.fileIdentifier),
        similarityScore(query, r.name),
        similarityScore(query, r.uuid),
      )
      return { ...r, score }
    })
    .sort((a, b) => (b.score - a.score) || a.fileIdentifier.localeCompare(b.fileIdentifier))
  if (!String(query || '').trim()) return ranked.slice(0, top)
  return ranked.filter((r) => r.score >= 0.25).slice(0, top)
}

function writeUuidFile(filePath, selected, query, recordGroup) {
  const lines = [
    '# Auto-generated by scripts/swarm/search-comet-similar.mjs',
    `# recordGroup: ${recordGroup}`,
    `# query: ${query || '(none)'}`,
    `# generatedAt: ${new Date().toISOString()}`,
    ...selected.map((r) => r.uuid),
    '',
  ]
  fs.mkdirSync(path.dirname(filePath), { recursive: true })
  fs.writeFileSync(filePath, lines.join('\n'), 'utf8')
}

async function main() {
  const args = parseArgs(process.argv.slice(2))
  if (!args.sessionId) {
    console.error('Missing COMET_SESSION_ID (or --session-id).')
    process.exit(1)
  }
  if (!args.recordGroup.trim()) {
    console.error('Missing --record-group.')
    process.exit(1)
  }
  const rows = await searchComet(args)
  const selected = selectTop(rows, args.query, Math.max(1, args.top))
  const report = {
    generatedAt: new Date().toISOString(),
    recordGroup: args.recordGroup,
    query: args.query || '',
    totalFound: rows.length,
    selectedCount: selected.length,
    selected,
  }
  fs.mkdirSync(path.dirname(args.outJson), { recursive: true })
  fs.writeFileSync(args.outJson, JSON.stringify(report, null, 2), 'utf8')
  writeUuidFile(args.outUuidFile, selected, args.query, args.recordGroup)
  console.log(`CoMET search results written: ${args.outJson}`)
  console.log(`UUID file written: ${args.outUuidFile}`)
  console.log(`Selected ${selected.length}/${rows.length} record(s).`)
}

main().catch((err) => {
  console.error(err instanceof Error ? err.message : String(err))
  process.exit(1)
})
